def set_locale(locale: str) -> str:
    """Set the active locale, falling back to English for unknown codes."""
    global _LOCALE
    _LOCALE = locale if locale in _TEXT else "en"
    return _LOCALE


def get_locale() -> str:
    """Return the active locale code."""
    return _LOCALE


def tr(key: str, **kwargs: object) -> str:
    """Translate a key using the active locale."""
    table = _TEXT.get(_LOCALE, _TEXT["en"])
    fallback = _TEXT["en"]
    template = table.get(key, fallback.get(key, key))
    return template.format(**kwargs)
